info = '假日是用來充實自己 不是用來偷懶的'
# 編碼
print(info.encode(
    'utf8'))  # bytes類型在python直接看成二進制即可 # b'\xe5\x81\x87\xe6\x97\xa5\xe6\x98\xaf\xe7\x94\xa8\xe4\xbe\x86\xe5\x85\x85\xe5\xaf\xa6\xe8\x87\xaa\xe5\xb7\xb1 \xe4\xb8\x8d\xe6\x98\xaf\xe7\x94\xa8\xe4\xbe\x86\xe5\x81\xb7\xe6\x87\xb6\xe7\x9a\x84'
res = info.encode('utf8')

# 解碼
res1 = res.decode('utf8')
print(res1)  # 假日是用來充實自己 不是用來偷懶的
